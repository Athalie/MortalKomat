const getTextLog = (type, { name: playerName1 }, { name: playerName2 }, timestamp) => {
  const range = logs[type].length;
  let text = '';
  switch (type) {
    case 'start':
      return text = logs[type].replace(/\[time\]/, timestamp).replace(/\[player1\]/, playerName1)
        .replace(/\[player2\]/, playerName2);
    case 'hit':
    case 'defence':
      return text = logs[type][getRandom(range)].replace(/\[playerKick\]/, playerName1)
        .replace(/\[playerDefence\]/, playerName2).replace('[time]', timestamp);
    case 'end':
      return text = logs[type][getRandom(range)].replace(/\[playerWins\]/, playerName1)
        .replace(/\[playerLose\]/, playerName2).replace('[time]', timestamp);
    case 'draw':
      return text = logs[type];
  }
};

/**
 * Генерация лога
 */
const generateLogs = (type, player1, player2, damage = 0) => {
  const timestamp = new Date().toLocaleTimeString().replace(/:..$/, '');
  const text = getTextLog(type, player1, player2, timestamp);
  let logItem = null;
  switch (type) {
    case 'start':
      logItem = `<p>${ timestamp } ${ text }</p>`;
      break;
    case 'hit':
    case 'defence':
      logItem = `<p>${ timestamp } ${ text } -${ damage } [${ player2.hp } / 100]</p>`;
      break;
    case 'end':
      logItem = `<p>${ timestamp } ${ text } -${ damage } [${ player2.hp } / 100]</p>`;
      break;
    case 'draw':
      logItem = `<p>${ timestamp } ${ text }</p>`;
  }

  $chat.insertAdjacentHTML('afterbegin', logItem);
};

export { generateLogs };
